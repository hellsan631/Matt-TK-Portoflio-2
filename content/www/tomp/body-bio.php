<p>
Tom Perlongo, a professional freelance musician, from Milwaukee, WI, has 29 years of guitar playing experience to his name. He has played with several top cover bands across Wisconsin & Illinois and is currently endorsing Haywire Custom Guitars.  He is influenced by guitar legends such as : Stevie Ray Vaughan, Joe Bonamassa, and Eric Clapton as well as other local Milwaukee musicians. His guitar of choice, "The Violator", a custom built Stratocaster, was proudly created by: Haywire Custom Guitars.<br />
<br />
Tom is also the Founder & CEO of FireyCat Studios of Milwaukee, WI. which was the proud recipient of a highly accomplished award from "The Society of Professional and Recording Services" in 2010.<br />
